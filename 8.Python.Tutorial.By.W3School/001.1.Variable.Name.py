# Variable Namaes Rule: Name must start with a letter or the undersocre character
#                     : Name can't be started with  a number
#                     : Name can only contain alpha-Numeric character & underscores(A-Z,0-9,_)
#                     : Names are case-sensitive

# Illegal Variable name : '-' in the middle of the name
#                       : space between the name. 

# Multi Words Variable Names : 
# 1] Camel Case : - myVariableName = "Jonny"
# 2] Pascal Case : - MyVariableName = "Jonny"
# 3] Snake Case : - my_variable_name = "Jonny"