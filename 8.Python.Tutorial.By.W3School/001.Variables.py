# Variables: Variables are containers for storing data values.
# A variable is created the moment you first assign a value to it.
x = 5
y = "John"
print(x)
print(y)

#Casting: If you want to specify the data type of a veriable, this can be done with casting.
x = str(3)
y = int(3)
z = float(8)
print(x)
print(y)
print(z)

# We can get to know the data type of a variable with the 'type()'.
x = 5
y = "John"
print(type(x))
print(type(y))

# Case Sensitive : Variable names are case-sensitive.
a = 3
A = "Sally"
print(a)
print(A)
